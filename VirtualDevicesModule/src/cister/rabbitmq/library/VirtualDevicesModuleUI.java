    /**
     *
     * @param componentName
     * @param componentGUI
     */
    public void addComponentToGUI(String componentName, SimpleGUI componentGUI)
    {
        final SimpleGUI c = componentGUI;
        JPanel panel = new JPanel(new GridLayout(1, 2));
        panel.setBorder(new EmptyBorder(3, 3, 3, 3) );
        
        JLabel componentTitle = new JLabel(componentName);
        componentTitle.setBorder(new EmptyBorder(0, 35, 0, 30));
        
        JButton componentShowHide = new JButton("Show/Hide");
        
        componentShowHide.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent ae)
            {
                if (c.isVisible())
                {
                    c.setVisible(false);
                }
                else
                {
                    c.setVisible(true);
                }
            }
        });
        
        panel.add(componentTitle, BorderLayout.LINE_START);
        panel.add(componentShowHide, BorderLayout.LINE_END);
        jPanel2.add(panel);
    }

