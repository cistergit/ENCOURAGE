    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jMenuItem1ActionPerformed
    {//GEN-HEADEREND:event_jMenuItem1ActionPerformed
        JFrame aboutFrame = new JFrame("About");
        aboutFrame.setSize(850, 250);
        aboutFrame.setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);

        JPanel aboutPanel = new JPanel();
        BufferedImage logoCister = null;
        BufferedImage logoInescTec = null;
        BufferedImage logoISEP = null;
        try
        {
            logoCister = ImageIO.read(new File("images/logo_cister.jpg"));
            logoInescTec = ImageIO.read(new File("images/logo_inesctec.png"));
            logoISEP = ImageIO.read(new File("images/logo_isep.png"));
        }
        catch (IOException ex)
        {
            Logger.getLogger(SimpleGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        JLabel picLabelCister = new JLabel(new ImageIcon( logoCister ));
        JLabel picLabelINESCTEC = new JLabel(new ImageIcon( logoInescTec ));
        JLabel picLabelISEP = new JLabel(new ImageIcon( logoISEP ));
        aboutPanel.add(picLabelCister);
        aboutPanel.add(picLabelINESCTEC);
        aboutPanel.add(picLabelISEP);
        
        String aboutContent = "<html><br /><br /><u><b>Virtual Devices Module</b></u><br /><br />Developed @ CISTER by: <br /><ul><li>César Teixeira</li><li>Michele Albano</li></ul></html>";
        JLabel aboutLabel = new JLabel(aboutContent);

        aboutPanel.add(aboutLabel);
        aboutFrame.add(aboutPanel);
        aboutFrame.setVisible(true);
    }//GEN-LAST:event_jMenuItem1ActionPerformed

